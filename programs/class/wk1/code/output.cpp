#include <iostream>
#include <string>

int main() {
  // these are numeric types
  int n;
  double x; 
  int y = 0;
  double z = 0.0; 



  std::cout << "Hello world!\n";
  std::cout << x << std::endl;
  std::cout << n << std::endl;
  std::cout << y << std::endl;
  std::cout << z << std::endl;



  // we also have chars (built-in type)
  char c = 'c';
  // string is not 
  std::string s = "Zaddy";
  std::cout << c << std::endl;
  std::cout << s << std::endl;

  return 0;
}
